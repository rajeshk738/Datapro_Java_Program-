package Jar_File_Creation;

public class Jar_file_example {
	
	
	/* In Eclipse Jar Creation
	 * 
	 * File ---> Export----> Select Java Jar---> select project resources ----> give jar file creation directory and name.
	 * 
	 * Jar file is created. And go the location and check the jar file
	 * 
	 * open cmd pointing to the jar file location creation
	 * 
	 *execute some commands related to jar
	 *
	 *jar ft example.jar   ---> shows table format contents of jar
	 *
	 *jar xf example.jar  ----> extracts the contents of the jar file.
	 */
	
	
	/* Using Command prompt jar creation
	 * 
	 * 
	 * open cmd at the location where java files( package of classes) are stored   C:\Users\Rajesh\Desktop\Pack
	 * 
	 * example : C:\Users\Rajesh\Desktop\Pack\myPack1 ---> here some java files are there in myPack1 package
	 * 
	 * execute some commands related to jar
	 * 
	 * point location  C:\Users\Rajesh\Desktop\Pack
	 * 
	 *    jar cf Example.jar myPack1\*.class    ---> creates Example.jar file containing java files of myPack1 package folder
	 *    
	 *jar ft example.jar   ---> shows table format contents of jar
	 *
	 *jar xf example.jar  ----> extracts the contents of the jar file.
	 */
	
	

}
